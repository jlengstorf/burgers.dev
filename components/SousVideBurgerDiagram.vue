<script>
import gsap from 'gsap';

export default {
  methods: {
    pathAnim(node) {
      const path = document.querySelectorAll(node);
      const lineLengthArr = Array.from(path, (line) => line.getTotalLength());

      path.forEach((el, i) => {
        gsap.set(el, {
          strokeDashoffset: lineLengthArr[i],
          strokeDasharray: lineLengthArr[i],
        });
      });

      gsap.to(path, 0.5, {
        strokeDashoffset: 0,
        delay: 1,
      });
    },
    burgerIntro() {
      gsap.set('.burgerimg image', {
        transformOrigin: '50% 50%',
      });

      gsap
        .timeline({
          defaults: {
            ease: 'elastic.out(1, 0.75)',
            duration: 1.25,
            delay: 0.5,
            scale: 0.5,
            opacity: 0.2,
          },
        })
        .add('start')
        .from(
          '.b1',
          {
            rotation: -10,
            y: 170,
          },
          'start',
        )
        .from(
          '.b2',
          {
            rotation: -20,
            y: 200,
          },
          'start+=0.05',
        )
        .from(
